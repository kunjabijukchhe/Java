public class Ifelse{
    public static void main(String[] args)
    {
     int a=10;
   
     int b=20;
     int c=30;
     if(a>b && a>c)
     {
         System.out.println("The greatest number is a i.e. "+a);
    
    }
    else if(b>c)
    {
        System.out.println("The greatest number is b i.e. "+b);
    }
    else
    {
        System.out.println("The greatest number is c i.e. "+c);
    }
}
       
 
}